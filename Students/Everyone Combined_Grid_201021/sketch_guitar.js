class Guitar {

  constructor(incomingShape) {
    this.incomingShape = incomingShape;
  }

  drawCircle() {
    strokeWeight(0)
    fill(this.incomingShape.color)
    push();
    if(this.incomingShape.circleX < 0 || this.incomingShape.circleX > width) {
      this.incomingShape.SpeedX = this.incomingShape.SpeedX *-1;
    }
    this.incomingShape.circleX = this.incomingShape.circleX + this.incomingShape.SpeedX;
    if(this.incomingShape.circleY < 0 || this.incomingShape.circleY > height) {
      this.incomingShape.SpeedY = this.incomingShape.SpeedY *-1;
    }
    this.incomingShape.circleY = this.incomingShape.circleY + this.incomingShape.SpeedY;
    translate(this.incomingShape.circleX,this.incomingShape.circleY)
    circle(0,0,this.incomingShape.circleDiameter)
    pop();
  }
  traceCircle(){
    push();
    strokeWeight(2)
    stroke(0);
    line(this.incomingShape.circleX,0, this.incomingShape.circleX, height);
    line(0,this.incomingShape.circleY, width, this.incomingShape.circleY);
    pop();
  }

  drawRect() {
    strokeWeight(0)
    fill(this.incomingShape.color);
    push();
    translate(this.incomingShape.rectX,this.incomingShape.rectY);
    this.incomingShape.rectW = this.incomingShape.rectW + this.incomingShape.Speed/10;
    this.incomingShape.rectH = this.incomingShape.rectH + this.incomingShape.Speed/10;
    if(this.incomingShape.rectW < 10 || this.incomingShape.rectW > 100) {
      this.incomingShape.Speed = this.incomingShape.Speed * -1;
    }
    rect(0,0,this.incomingShape.rectW, this.incomingShape.rectH);
    pop();
  }
  drawQuad(){
    strokeWeight(0)
    fill(this.incomingShape.color)
    push();
    translate(this.incomingShape.transX,this.incomingShape.transY)
    // rotate(this.incomingShape.Angle);
    this.incomingShape.Angle = this.incomingShape.Angle + this.incomingShape.Speed* 0.01;
    quad(this.incomingShape.x1,this.incomingShape.y1,this.incomingShape.x2,this.incomingShape.y2,this.incomingShape.x3,this.incomingShape.y3,this.incomingShape.x4,this.incomingShape.y4);
    pop();
  }
}
