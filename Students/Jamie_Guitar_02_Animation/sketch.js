  function drawCircle(incomingCircle) {
  fill(incomingCircle.color)
  push();
  if(incomingCircle.circleX < 0 || incomingCircle.circleX > width) {
  incomingCircle.SpeedX = incomingCircle.SpeedX *-1;
  }
  incomingCircle.circleX = incomingCircle.circleX + incomingCircle.SpeedX;
  if(incomingCircle.circleY < 0 || incomingCircle.circleY > height) {
  incomingCircle.SpeedY = incomingCircle.SpeedY *-1;
  }
  incomingCircle.circleY = incomingCircle.circleY + incomingCircle.SpeedY;
  translate(incomingCircle.circleX,incomingCircle.circleY)
  circle(0,0,incomingCircle.circleDiameter)
  pop();
  }

  function drawRect(incomingRect) {
  fill(incomingRect.color);
  push();
  translate(incomingRect.rectX,incomingRect.rectY);
  incomingRect.rectW = incomingRect.rectW + incomingRect.Speed/10;
  incomingRect.rectH = incomingRect.rectH + incomingRect.Speed/10;
  if(incomingRect.rectW < 10 || incomingRect.rectW > 100) {
  incomingRect.Speed = incomingRect.Speed * -1;
  }
  rect(0,0,incomingRect.rectW, incomingRect.rectH);
  pop();
  }

  function drawQuad(incomingQuad){
  fill(incomingQuad.color)
  push();
  translate(incomingQuad.transX,incomingQuad.transY)
  rotate(incomingQuad.Angle);
  incomingQuad.Angle = incomingQuad.Angle + incomingQuad.Speed* 0.01;
  quad(incomingQuad.x1,incomingQuad.y1,incomingQuad.x2,incomingQuad.y2,incomingQuad.x3,incomingQuad.y3,incomingQuad.x4,incomingQuad.y4);
  pop();
  }
